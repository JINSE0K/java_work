package ch01;

public class Datatype5 {
	public static void main(String[] args) {
		//논리형 boolean 불리언 ( 참 거짓 타입)
		
		boolean isMarried = false;
		// ======= 위에 불리언타입에 변수명 지을때는 is를 붙혀야함 그냥 붙이셈 ======
		System.out.println(isMarried);
		isMarried = true;
		// =========== 변수는 말그대로 변할수있는 수 라서 중간에 바꿔줘도 노상관 ======
		System.out.println(isMarried);
	}
}
